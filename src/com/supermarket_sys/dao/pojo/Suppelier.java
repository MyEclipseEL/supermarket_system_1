package com.supermarket_sys.dao.pojo;

public class Suppelier {
    private Integer suo_id;
    private String sup_phone;
    private String sup_address;
    private String sup_name;
    private String sup_remark;

    public Integer getSuo_id() {
        return suo_id;
    }

    public void setSuo_id(Integer suo_id) {
        this.suo_id = suo_id;
    }

    public String getSup_phone() {
        return sup_phone;
    }

    public void setSup_phone(String sup_phone) {
        this.sup_phone = sup_phone;
    }

    public String getSup_address() {
        return sup_address;
    }

    public void setSup_address(String sup_address) {
        this.sup_address = sup_address;
    }

    public String getSup_name() {
        return sup_name;
    }

    public void setSup_name(String sup_name) {
        this.sup_name = sup_name;
    }

    public String getSup_remark() {
        return sup_remark;
    }

    public void setSup_remark(String sup_remark) {
        this.sup_remark = sup_remark;
    }
}
